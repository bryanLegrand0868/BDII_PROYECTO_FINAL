-- ============================================================
--  MÓDULO 1 — Usuarios de la Aplicación
-- ============================================================
--  Almacena los administradores que usan la herramienta web.
--  NO son usuarios Oracle; la app se conecta a Oracle con un
--  único usuario de servicio y ejecuta los DCL desde el backend.
-- ============================================================

CREATE TABLE APP_USERS (
    user_id         NUMBER          GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    username        VARCHAR2(50)    NOT NULL,
    password_hash   VARCHAR2(255)   NOT NULL,   -- bcrypt / SHA-256 desde el backend
    email           VARCHAR2(100)   NOT NULL,
    full_name       VARCHAR2(150)   NOT NULL,
    status          VARCHAR2(20)    DEFAULT 'ACTIVO'
                        CONSTRAINT ck_users_status
                        CHECK (status IN ('ACTIVO','INACTIVO','BLOQUEADO')),
    app_role        VARCHAR2(20)    DEFAULT 'VIEWER'
                        CONSTRAINT ck_users_approle
                        CHECK (app_role IN ('SUPERADMIN','ADMIN','VIEWER')),
    created_at      TIMESTAMP       DEFAULT SYSTIMESTAMP NOT NULL,
    last_login      TIMESTAMP,
    CONSTRAINT uq_users_username UNIQUE (username),
    CONSTRAINT uq_users_email    UNIQUE (email)
);

ALTER TABLE APP_USERS ADD oracle_username VARCHAR2(128);

COMMENT ON TABLE  APP_USERS              IS 'Administradores de la herramienta DCL Manager';
COMMENT ON COLUMN APP_USERS.app_role     IS 'SUPERADMIN: todo | ADMIN: gestión | VIEWER: solo lectura';
COMMENT ON COLUMN APP_USERS.status       IS 'ACTIVO | INACTIVO | BLOQUEADO';
COMMENT ON COLUMN APP_USERS.password_hash IS 'Hash generado en el backend; nunca contraseña en claro';


-- ============================================================
--  MÓDULO 2 — Roles y Jerarquía
-- ============================================================
--  Modela los roles Oracle (CREATE ROLE) y su herencia.
--  parent_role_id = NULL indica rol raíz (sin herencia).
-- ============================================================

CREATE TABLE APP_ROLES (
    role_id         NUMBER          GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    role_name       VARCHAR2(100)   NOT NULL,
    description     VARCHAR2(500),
    parent_role_id  NUMBER,                     -- auto-referencia para herencia
    is_oracle_role  CHAR(1)         DEFAULT 'N'
                        CONSTRAINT ck_roles_isoracle
                        CHECK (is_oracle_role IN ('S','N')),
    created_at      TIMESTAMP       DEFAULT SYSTIMESTAMP NOT NULL,
    created_by      NUMBER,
    CONSTRAINT uq_roles_name    UNIQUE (role_name),
    CONSTRAINT fk_roles_parent  FOREIGN KEY (parent_role_id)
        REFERENCES APP_ROLES (role_id),
    CONSTRAINT fk_roles_creator FOREIGN KEY (created_by)
        REFERENCES APP_USERS (user_id)
);

COMMENT ON TABLE  APP_ROLES               IS 'Roles Oracle gestionados por la aplicación';
COMMENT ON COLUMN APP_ROLES.parent_role_id IS 'FK recursiva: rol padre en la jerarquía';
COMMENT ON COLUMN APP_ROLES.is_oracle_role IS 'S = ya existe en Oracle; N = pendiente de aplicar';


-- ============================================================
--  MÓDULO 3 — Permisos / Privilegios
-- ============================================================
--  Catálogo de los privilegios disponibles.
--  Representa los objetos sobre los que se puede hacer GRANT.
-- ============================================================

CREATE TABLE APP_PERMISSIONS (
    permission_id   NUMBER          GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    object_name     VARCHAR2(128)   NOT NULL,   -- nombre de tabla, vista, proc, etc.
    object_type     VARCHAR2(30)    NOT NULL
                        CONSTRAINT ck_perms_objtype
                        CHECK (object_type IN (
                            'TABLE','VIEW','SEQUENCE','PROCEDURE',
                            'FUNCTION','PACKAGE','SCHEMA','SYSTEM')),
    privilege_type  VARCHAR2(50)    NOT NULL,   -- SELECT, INSERT, EXECUTE, etc.
    schema_name     VARCHAR2(128)   NOT NULL,   -- propietario del objeto en Oracle
    description     VARCHAR2(300),
    CONSTRAINT uq_perms_combo UNIQUE (schema_name, object_name, privilege_type)
);

COMMENT ON TABLE  APP_PERMISSIONS              IS 'Catálogo de privilegios disponibles para asignar';
COMMENT ON COLUMN APP_PERMISSIONS.object_type  IS 'Tipo de objeto Oracle';
COMMENT ON COLUMN APP_PERMISSIONS.privilege_type IS 'SELECT | INSERT | UPDATE | DELETE | EXECUTE | ALL';
COMMENT ON COLUMN APP_PERMISSIONS.schema_name   IS 'Esquema Oracle dueño del objeto';


-- ============================================================
--  MÓDULO 4 — Relaciones de Asignación
-- ============================================================
--  4A: Roles asignados a usuarios de la app
--  4B: Permisos asignados a roles
--  4C: Permisos directos asignados a usuarios
-- ============================================================

-- 4A — Roles asignados a usuarios de la aplicación
CREATE TABLE USER_ROLES (
    user_id         NUMBER          NOT NULL,
    role_id         NUMBER          NOT NULL,
    assigned_at     TIMESTAMP       DEFAULT SYSTIMESTAMP NOT NULL,
    assigned_by     NUMBER          NOT NULL,   -- quién hizo la asignación
    expires_at      TIMESTAMP,                  -- NULL = sin expiración
    is_active       CHAR(1)         DEFAULT 'S'
                        CONSTRAINT ck_ur_active
                        CHECK (is_active IN ('S','N')),
    CONSTRAINT pk_user_roles    PRIMARY KEY (user_id, role_id),
    CONSTRAINT fk_ur_user       FOREIGN KEY (user_id)
        REFERENCES APP_USERS (user_id) ON DELETE CASCADE,
    CONSTRAINT fk_ur_role       FOREIGN KEY (role_id)
        REFERENCES APP_ROLES (role_id) ON DELETE CASCADE,
    CONSTRAINT fk_ur_assignedby FOREIGN KEY (assigned_by)
        REFERENCES APP_USERS (user_id)
);

COMMENT ON TABLE USER_ROLES IS 'Asignación de roles a usuarios de la aplicación';


-- 4B — Permisos asignados a roles (GRANT <priv> ON <obj> TO <role>)
CREATE TABLE ROLE_PERMISSIONS (
    role_id         NUMBER          NOT NULL,
    permission_id   NUMBER          NOT NULL,
    grant_option    CHAR(1)         DEFAULT 'N'
                        CONSTRAINT ck_rp_grantoption
                        CHECK (grant_option IN ('S','N')),  -- WITH GRANT OPTION
    granted_at      TIMESTAMP       DEFAULT SYSTIMESTAMP NOT NULL,
    granted_by      NUMBER          NOT NULL,
    CONSTRAINT pk_role_permissions  PRIMARY KEY (role_id, permission_id),
    CONSTRAINT fk_rp_role           FOREIGN KEY (role_id)
        REFERENCES APP_ROLES (role_id) ON DELETE CASCADE,
    CONSTRAINT fk_rp_permission     FOREIGN KEY (permission_id)
        REFERENCES APP_PERMISSIONS (permission_id) ON DELETE CASCADE,
    CONSTRAINT fk_rp_grantedby      FOREIGN KEY (granted_by)
        REFERENCES APP_USERS (user_id)
);

COMMENT ON TABLE  ROLE_PERMISSIONS             IS 'Permisos asignados a roles (genera GRANT ... TO role)';
COMMENT ON COLUMN ROLE_PERMISSIONS.grant_option IS 'S = WITH GRANT OPTION en el SQL generado';


-- 4C — Permisos directos a usuarios (GRANT <priv> ON <obj> TO <oracle_user>)
CREATE TABLE USER_PERMISSIONS (
    user_id         NUMBER          NOT NULL,
    permission_id   NUMBER          NOT NULL,
    grant_option    CHAR(1)         DEFAULT 'N'
                        CONSTRAINT ck_up_grantoption
                        CHECK (grant_option IN ('S','N')),
    granted_at      TIMESTAMP       DEFAULT SYSTIMESTAMP NOT NULL,
    granted_by      NUMBER          NOT NULL,
    oracle_username VARCHAR2(128)   NOT NULL,   -- usuario Oracle destino del GRANT
    CONSTRAINT pk_user_permissions  PRIMARY KEY (user_id, permission_id),
    CONSTRAINT fk_up_user           FOREIGN KEY (user_id)
        REFERENCES APP_USERS (user_id) ON DELETE CASCADE,
    CONSTRAINT fk_up_permission     FOREIGN KEY (permission_id)
        REFERENCES APP_PERMISSIONS (permission_id) ON DELETE CASCADE,
    CONSTRAINT fk_up_grantedby      FOREIGN KEY (granted_by)
        REFERENCES APP_USERS (user_id)
);

COMMENT ON TABLE  USER_PERMISSIONS              IS 'Permisos directos a usuarios Oracle (sin pasar por rol)';
COMMENT ON COLUMN USER_PERMISSIONS.oracle_username IS 'Usuario Oracle destino (puede diferir del username de la app)';


-- ============================================================
--  MÓDULO 5 — Auditoría y Scripts SQL Generados
-- ============================================================

-- 5A — Log de auditoría de cada acción administrativa
CREATE TABLE AUDIT_LOG (
    log_id          NUMBER          GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    actor_user_id   NUMBER          NOT NULL,   -- quién ejecutó la acción
    action_type     VARCHAR2(30)    NOT NULL
                        CONSTRAINT ck_audit_action
                        CHECK (action_type IN (
                            'GRANT','REVOKE','CREATE_ROLE','DROP_ROLE',
                            'CREATE_USER','DROP_USER','MODIFY_USER',
                            'ASSIGN_ROLE','REVOKE_ROLE','LOGIN','LOGIN_FAILED')),
    target_type     VARCHAR2(30)    NOT NULL
                        CONSTRAINT ck_audit_target
                        CHECK (target_type IN (
                            'USER','ROLE','PERMISSION','SYSTEM')),
    target_id       NUMBER,                     -- ID del registro afectado
    target_name     VARCHAR2(255),              -- nombre legible del objetivo
    sql_generated   CLOB,                       -- SQL DCL que se produjo
    performed_at    TIMESTAMP       DEFAULT SYSTIMESTAMP NOT NULL,
    ip_address      VARCHAR2(45),               -- IPv4 o IPv6
    result          VARCHAR2(10)    DEFAULT 'OK'
                        CONSTRAINT ck_audit_result
                        CHECK (result IN ('OK','ERROR')),
    error_detail    VARCHAR2(1000),             -- mensaje de error si result = ERROR
    CONSTRAINT fk_audit_actor FOREIGN KEY (actor_user_id)
        REFERENCES APP_USERS (user_id)
);

COMMENT ON TABLE  AUDIT_LOG             IS 'Registro inmutable de todas las acciones administrativas';
COMMENT ON COLUMN AUDIT_LOG.sql_generated IS 'SQL DCL exacto que se generó y/o ejecutó';
COMMENT ON COLUMN AUDIT_LOG.result       IS 'OK = exitoso | ERROR = falló (ver error_detail)';


-- 5B — Scripts SQL generados automáticamente para aplicar en Oracle
CREATE TABLE SQL_SCRIPTS (
    script_id       NUMBER          GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    generated_by    NUMBER          NOT NULL,
    script_type     VARCHAR2(30)    NOT NULL
                        CONSTRAINT ck_scripts_type
                        CHECK (script_type IN (
                            'GRANT','REVOKE','CREATE_ROLE',
                            'DROP_ROLE','MIXED')),
    script_content  CLOB            NOT NULL,   -- bloque SQL completo listo para ejecutar
    description     VARCHAR2(500),
    generated_at    TIMESTAMP       DEFAULT SYSTIMESTAMP NOT NULL,
    applied         CHAR(1)         DEFAULT 'N'
                        CONSTRAINT ck_scripts_applied
                        CHECK (applied IN ('S','N')),
    applied_at      TIMESTAMP,
    applied_by      NUMBER,
    CONSTRAINT fk_scripts_generatedby FOREIGN KEY (generated_by)
        REFERENCES APP_USERS (user_id),
    CONSTRAINT fk_scripts_appliedby   FOREIGN KEY (applied_by)
        REFERENCES APP_USERS (user_id)
);

COMMENT ON TABLE  SQL_SCRIPTS               IS 'Scripts DCL generados por la aplicación para ejecutar en Oracle';
COMMENT ON COLUMN SQL_SCRIPTS.script_content IS 'SQL completo: puede contener múltiples sentencias DCL';
COMMENT ON COLUMN SQL_SCRIPTS.applied        IS 'S = ya aplicado en Oracle; N = pendiente';


-- ============================================================
--  MÓDULO 6 — Índices de rendimiento
