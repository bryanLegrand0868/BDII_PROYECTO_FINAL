CREATE INDEX idx_audit_actor    ON AUDIT_LOG     (actor_user_id, performed_at DESC);
CREATE INDEX idx_audit_target   ON AUDIT_LOG     (target_type, target_id);
CREATE INDEX idx_audit_date     ON AUDIT_LOG     (performed_at DESC);
CREATE INDEX idx_ur_role        ON USER_ROLES    (role_id);
CREATE INDEX idx_rp_perm        ON ROLE_PERMISSIONS (permission_id);
CREATE INDEX idx_up_oracle_user ON USER_PERMISSIONS (oracle_username);
CREATE INDEX idx_scripts_date   ON SQL_SCRIPTS   (generated_at DESC);
CREATE INDEX idx_scripts_pending ON SQL_SCRIPTS (applied);   -- índice parcial: sólo pendientes


-- ============================================================
--  MÓDULO 7 — Triggers de Auditoría Automática
-- ============================================================

-- 7A: Registro automático de cambios en APP_USERS
CREATE OR REPLACE TRIGGER trg_audit_app_users
    AFTER INSERT OR UPDATE OR DELETE ON APP_USERS
    FOR EACH ROW
DECLARE
    v_action    VARCHAR2(30);
    v_target    VARCHAR2(255);
BEGIN
    IF INSERTING THEN
        v_action := 'CREATE_USER';
        v_target := :NEW.username;
    ELSIF UPDATING THEN
        v_action := 'MODIFY_USER';
        v_target := :NEW.username;
    ELSE
        v_action := 'DROP_USER';
        v_target := :OLD.username;
    END IF;

    INSERT INTO AUDIT_LOG (
        actor_user_id, action_type, target_type,
        target_id, target_name, performed_at, result
    ) VALUES (
        NVL(:NEW.user_id, :OLD.user_id),
        v_action, 'USER',
        NVL(:NEW.user_id, :OLD.user_id),
        v_target, SYSTIMESTAMP, 'OK'
    );
END;
/

-- 7B: Registro automático de cambios en APP_ROLES
CREATE OR REPLACE TRIGGER trg_audit_app_roles
    AFTER INSERT OR DELETE ON APP_ROLES
    FOR EACH ROW
DECLARE
    v_action VARCHAR2(30);
BEGIN
    v_action := CASE WHEN INSERTING THEN 'CREATE_ROLE' ELSE 'DROP_ROLE' END;

    INSERT INTO AUDIT_LOG (
        actor_user_id, action_type, target_type,
        target_id, target_name, performed_at, result
    ) VALUES (
        NVL(:NEW.created_by, :OLD.created_by),
        v_action, 'ROLE',
        NVL(:NEW.role_id, :OLD.role_id),
        NVL(:NEW.role_name, :OLD.role_name),
        SYSTIMESTAMP, 'OK'
    );
END;
/

-- 7C: Registro de asignación / revocación de roles a usuarios
CREATE OR REPLACE TRIGGER trg_audit_user_roles
    AFTER INSERT OR DELETE ON USER_ROLES
    FOR EACH ROW
DECLARE
    v_action VARCHAR2(30);
BEGIN
    v_action := CASE WHEN INSERTING THEN 'ASSIGN_ROLE' ELSE 'REVOKE_ROLE' END;

    INSERT INTO AUDIT_LOG (
        actor_user_id, action_type, target_type,
        target_id, target_name, performed_at, result
    ) VALUES (
        NVL(:NEW.assigned_by, :OLD.assigned_by),
        v_action, 'ROLE',
        NVL(:NEW.role_id, :OLD.role_id),
        'USER_ID=' || NVL(:NEW.user_id, :OLD.user_id) ||
            ' ROLE_ID=' || NVL(:NEW.role_id, :OLD.role_id),
        SYSTIMESTAMP, 'OK'
    );
END;
/


-- ============================================================
--  MÓDULO 8 — Vistas de Soporte para el Backend
-- ============================================================

-- Vista completa de permisos efectivos por usuario
--   (permisos directos + permisos heredados via roles)
CREATE OR REPLACE VIEW VW_USER_EFFECTIVE_PERMISSIONS AS
SELECT
    u.user_id,
    u.username,
    u.oracle_username,
    p.schema_name,
    p.object_name,
    p.object_type,
    p.privilege_type,
    up.grant_option,
    'DIRECTO' AS origin_type,
    NULL       AS via_role_name
FROM APP_USERS u
JOIN USER_PERMISSIONS up ON up.user_id = u.user_id
JOIN APP_PERMISSIONS  p  ON p.permission_id = up.permission_id

UNION ALL

SELECT
    u.user_id,
    u.username,
    u.oracle_username,
    p.schema_name,
    p.object_name,
    p.object_type,
    p.privilege_type,
    rp.grant_option,
    'ROL'      AS origin_type,
    r.role_name AS via_role_name
FROM APP_USERS u
JOIN USER_ROLES      ur ON ur.user_id = u.user_id AND ur.is_active = 'S'
JOIN APP_ROLES       r  ON r.role_id  = ur.role_id
JOIN ROLE_PERMISSIONS rp ON rp.role_id = r.role_id
JOIN APP_PERMISSIONS  p  ON p.permission_id = rp.permission_id;

COMMENT ON TABLE VW_USER_EFFECTIVE_PERMISSIONS IS
    'Permisos efectivos de cada usuario: directos + heredados por rol';


-- Vista de jerarquía de roles (nivel de anidamiento)
CREATE OR REPLACE VIEW VW_ROLE_HIERARCHY AS
SELECT
    r.role_id,
    r.role_name,
    r.parent_role_id,
    p.role_name  AS parent_role_name,
    r.is_oracle_role,
    r.description
FROM APP_ROLES r
LEFT JOIN APP_ROLES p ON p.role_id = r.parent_role_id;

COMMENT ON TABLE VW_ROLE_HIERARCHY IS 'Árbol de herencia de roles con nombre del padre';


-- Vista de auditoría enriquecida (join con username legible)
CREATE OR REPLACE VIEW VW_AUDIT_LOG AS
SELECT
    a.log_id,
    u.username      AS actor_username,
    a.action_type,
    a.target_type,
    a.target_name,
    a.result,
    a.error_detail,
    a.ip_address,
    a.performed_at,
    a.sql_generated
FROM AUDIT_LOG a
JOIN APP_USERS u ON u.user_id = a.actor_user_id
ORDER BY a.performed_at DESC;

COMMENT ON TABLE VW_AUDIT_LOG IS 'Log de auditoría con nombre de usuario legible';


-- Vista de scripts pendientes de aplicar
CREATE OR REPLACE VIEW VW_PENDING_SCRIPTS AS
SELECT
    s.script_id,
    u.username  AS generated_by_username,
    s.script_type,
    s.description,
    s.generated_at,
    s.script_content
FROM SQL_SCRIPTS s
JOIN APP_USERS u ON u.user_id = s.generated_by
WHERE s.applied = 'N'
ORDER BY s.generated_at;

COMMENT ON TABLE VW_PENDING_SCRIPTS IS 'Scripts DCL generados que aún no han sido aplicados en Oracle';


